on menu_feedback
for select
to anon
using (true);

create policy "Allow anon insert menu feedback"
on menu_feedback
for insert
to anon
with check (true);

create table if not exists toss_usage_events (
    id bigint generated by default as identity primary key,
    user_id text not null,
    event_name text not null check (
        event_name in (
            'app_open',
            'questions_loaded',
            'recommendation_completed',
            'feedback_clicked',
            'share_clicked',
            'restart_clicked'
        )
    ),
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now()
);

create index if not exists idx_toss_usage_events_user
on toss_usage_events (user_id);

create index if not exists idx_toss_usage_events_event_created_at
on toss_usage_events (event_name, created_at desc);

